//ColorHolder class with getters and setters
import java.awt.Color;

public class ColorHolder {
	//instance variable
	private Color color;
	
	//default constructor
	public ColorHolder() {
		color = Color.BLACK;
	}

	public Color getColor() {
		return color;
	}

	public void setColor(Color color) {
		this.color = color;
	}

}
